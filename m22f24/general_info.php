<h3 id="honor">The Honor Principle</h3></a>
<p>Academic integrity is at the core of our mission as mathematicians and educators, and we take it very seriously. We also believe in working and learning together.</p>

<p>Collaboration on homework is permitted and encouraged, but obviously it is a violation of the honor code for someone to provide the answers for you.</p>

<p>On written homework, you are encouraged to work together, and you may get help from others, but you must write up the answers yourself. If you are part of a group of students that produces an answer to a problem, you cannot then copy that group answer. You must write up the answer individually, in your own words.</p>

<p>On WeBWorK assignments, each person in the class has the potential to receive a homework assignment distinct from anyone else&apos;s, but all cut from the same mold. For example if the problem were to find the minimum of ax^2 + bx + c, each student would receive their own values of a, b, and c. A good way to work together, to work with a tutor, or to ask for help, is to log in to one of the practice accounts (see the Assignments Page) and work on those problems. Then, once you understand how to do the problems, log in to your own account and do the problems yourself.</p>

<p>On exams, you may not give or receive help from anyone. Exams in this course are closed book, and no notes, calculators or other electronic devices are permitted.</p>

